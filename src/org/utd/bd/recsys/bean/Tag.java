package org.utd.bd.recsys.bean;

public class Tag {

	private String title;
	private int count; //number of artists tagged

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
